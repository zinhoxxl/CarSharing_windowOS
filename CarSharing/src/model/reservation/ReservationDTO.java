package model.reservation;

public class ReservationDTO {
	
	private int reservationNo;
	private String carNumber;
	private String carName;
	private int retalFeePerDay;
	private String carPickupDate;
	private String carReturnDate;
	private String memberId;
	private String name;
	private String birth;
	private String phone;
	private String driverLicence;
	private int rentalDate;
	private int totalRentalFee;
	
	
	public int getReservationNo() {
		return reservationNo;
	}
	public void setReservationNo(int reservationNo) {
		this.reservationNo = reservationNo;
	}
	public String getCarNumber() {
		return carNumber;
	}
	public void setCarNumber(String carNumber) {
		this.carNumber = carNumber;
	}
	public String getCarName() {
		return carName;
	}
	public void setCarName(String carName) {
		this.carName = carName;
	}
	public int getRetalFeePerDay() {
		return retalFeePerDay;
	}
	public void setRetalFeePerDay(int retalFeePerDay) {
		this.retalFeePerDay = retalFeePerDay;
	}
	public String getCarPickupDate() {
		return carPickupDate;
	}
	public void setCarPickupDate(String carPickupDate) {
		this.carPickupDate = carPickupDate;
	}
	public String getCarReturnDate() {
		return carReturnDate;
	}
	public void setCarReturnDate(String carReturnDate) {
		this.carReturnDate = carReturnDate;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getDriverLicence() {
		return driverLicence;
	}
	public void setDriverLicence(String driverLicence) {
		this.driverLicence = driverLicence;
	}
	public int getRentalDate() {
		return rentalDate;
	}
	public void setRentalDate(int rentalDate) {
		this.rentalDate = rentalDate;
	}
	public int getTotalRentalFee() {
		return totalRentalFee;
	}
	public void setTotalRentalFee(int totalRentalFee) {
		this.totalRentalFee = totalRentalFee;
	}
	
	


}
